const html_specchars * is_special_char(char c)
{
	int i;

	i = 0;

	while(specialchars[i].ascii && (specialchars[i].ascii != c) )
	{
		i++;
	}

	if(specialchars[i].ascii)
	{
		return &specialchars[i];
	}

	return NULL;
}

